// comparison operators

// --> Relational operators
// --> Euality operators


// relation operators

// > (greater then) , < (less then) , <= , >= 


console.log(10 > 10);
console.log(10 < 10);

console.log(10 >= 10);
console.log(10 <= 10);


// Equality operators

// === (strict equality) , !== (strict non-equality) , == (loose equality) , !=(loose not quality)

console.log(10 === "10");
console.log(10 !== "10");

console.log(10 == "10");
console.log(10 != "10");



